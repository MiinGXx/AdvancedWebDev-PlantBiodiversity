        <h2>Tutorial</h2>
        <p>Herbarium specimens are preserved plant specimens that are used for scientific study and reference. These specimens are essential for understanding plant biodiversity, taxonomy, and evolution. Here is a step-by-step tutorial on how to create a herbarium specimen:</p>
        <div class="tutorial-steps">
            <div class="step">
                <h3>Step 1: Collect a Fresh Leaf</h3>
                <p>Choose a healthy leaf from a plant specimen. Make sure the leaf is fresh and free from any damage or disease.</p>
            </div>
            <div class="step">
                <h3>Step 2: Press the Leaf</h3>
                <p>Place the leaf between two sheets of newspaper or blotting paper. Apply pressure evenly on the leaf to flatten it.</p>
            </div>
            <div class="step">
                <h3>Step 3: Dry the Leaf</h3>
                <p>Leave the pressed leaf in a warm, dry place for a few days until it is completely dry and brittle.</p>
            </div>
            <div class="step">
                <h3>Step 4: Mount the Leaf</h3>
                <p>Glue the dried leaf onto a herbarium sheet. Include essential information like scientific name, collection date, and location.</p>
            </div>
            <div class="step">
                <h3>Step 5: Preserve the Herbarium</h3>
                <p>Store the herbarium specimen in a cool, dry place away from direct sunlight and moisture to prevent decay.</p>
            </div>
        </div>
